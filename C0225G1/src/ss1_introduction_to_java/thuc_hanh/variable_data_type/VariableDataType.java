package ss1_introduction_to_java.thuc_hanh.variable_data_type;

public class VariableDataType {
    public static void main (String[] args) {
        int i = 10;//khai báo biến có kiểu int
        float f = 20.5f;//khai báo biến có kiểu float
        double d = 20.5;//khai báo biến có kieeru double
        boolean b = true; //khai báo biến có kiểu boolean
        char c = 'c';//khai báo biến có kiểu char
        String s = "Hà Nội";//khai báo biến có kiểu String
        System.out.println("i = " +i);
        System.out.println("f = " +f);
        System.out.println("d = " +d);
        System.out.println("b = " +b);
        System.out.println("c = " +c);
        System.out.println("s = " +s);
    }
}
